    //Creates the Registry with default RMI port
    private void CreateRegistry() {
        try {   
            server = new Game(this);
            LocateRegistry.createRegistry(1099); //Create registry on default port
            Naming.rebind("Server", server);
        }
        catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }
    
    public void ServerActive() {
        statusLabel.setText("Server Ready");
    }
    
